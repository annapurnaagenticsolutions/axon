def test_python_module_entrypoint_build(tmp_path):
    source = tmp_path / "hello.ax"
    output = tmp_path / "server.py"
    source.write_text(SIMPLE_SOURCE, encoding="utf-8")

    completed = subprocess.run(
        [
            sys.executable,
            "-m",
            "axon",
            "build",
            str(source),
            "--output",
            str(output),
        ],
        check=False,
        capture_output=True,
        text=True,
        cwd=ROOT,
        env=_subprocess_env(),
    )

    assert completed.returncode == 0
    assert output.exists()
    assert "Generated MCP server:" in completed.stdout


