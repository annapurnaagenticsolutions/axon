import os
import subprocess
import sys
from pathlib import Path


ROOT = Path(__file__).resolve().parents[1]


def _subprocess_env() -> dict[str, str]:
    env = os.environ.copy()
    src = str(ROOT / "src")
    existing = env.get("PYTHONPATH")
    env["PYTHONPATH"] = src if not existing else f"{src}{os.pathsep}{existing}"
    return env

from axon.cli import build_file, build_to_stdout, main


SIMPLE_SOURCE = '''
tool Greet(name: Str) -> Str {
    /// Says hello.
    "Hello, {name}!"
}
agent Bot {
    model: @anthropic/claude-4
    tools: [Greet]
    fn run(q: Str) -> Str { q }
}
'''


def test_build_file_writes_explicit_output(tmp_path):
    source = tmp_path / "hello.ax"
    output = tmp_path / "server.py"
    source.write_text(SIMPLE_SOURCE, encoding="utf-8")

    result = build_file(source, output_path=output)

    assert result == output
    code = output.read_text(encoding="utf-8")
    assert "Generated by AXON v0.1" in code
    assert "from fastmcp import FastMCP" in code
    assert "def greet(name: str) -> str:" in code


